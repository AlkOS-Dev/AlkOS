#ifndef KERNEL_INCLUDE_TYPES_HPP_
#define KERNEL_INCLUDE_TYPES_HPP_

#include <stdbool.h>
#include <stddef.h>
#include <stdint.h>

/* simplified unsigned int types */
typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;
typedef u8 byte;

/* simplified int types */
typedef int8_t i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

/* simplified float types */
typedef float f32;
typedef double f64;

#endif  // KERNEL_INCLUDE_TYPES_HPP_
